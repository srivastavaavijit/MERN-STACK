// Primitive Data Types:
// Number
let num = 50;
console.log(typeof(num));

// String
let str = "Hello";
console.log(typeof(str));

// Boolean
let bool = true;
console.log(typeof(bool));

// null
let a = null;
console.log(typeof(a));

// undefined
let b;
console.log(typeof(b));


