// Arithmetic Operator:

// Addition:
console.log(2 + 5);

// Subtraction:
console.log(5 - 3);

// Multiplication:
console.log(5 * 4);

// Division:
console.log(40 / 8);

// Modulus:
console.log(46 % 5);

// Exponentiation Operator:
console.log(3 ** 2);

// Increment - Decrement:
let a = 1;
c = a++;
console.log(c);
console.log(a);

let b = 2;
d = ++b;
console.log(d);
console.log(b);


// Comparison Operator:

a = "6";
b = 6;
console.log(a === b);
console.log(a !== b);
console.log(a < b);
console.log(a > b);
console.log(a <= b);
console.log(a >= b);


// Logical Operator:
a = true;
b = false;
console.log(a && b);
console.log(a || b);

console.log(2>3 || 3<4);
console.log(!0);
